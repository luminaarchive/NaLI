#!/usr/bin/env node
/**
 * Editorial-content validator for NaLI.
 *
 * Fails the build if public content violates the trust rules:
 *  1. No em dash characters in public repository text.
 *  2. No public article or field note contains demo or placeholder language.
 *  3. No first-person fieldwork claims unless firstPartyFieldwork is true.
 *  4. Every published article has sourceIds, sources, claimLedger, limitations,
 *     confidence, evidenceBasis, firstPartyFieldwork: false, and a displayed
 *     licensed image or rendered internal explanatory diagram/map/timeline image.
 *  5. Every source has the archive metadata required by the deep archive sprint.
 *  6. Displayed images and internal diagrams have license metadata.
 *  7. External visual evidence is linked only and has source, limitation, and
 *     checkedAt metadata.
 *
 * Usage: node scripts/validate-editorial-content.mjs
 */
import fs from "node:fs";
import path from "node:path";
import matter from "gray-matter";
import { loadPublications } from "./load-publications.mjs";

const ROOT = process.cwd();
const ARTICLES = path.join(ROOT, "content", "articles");
const FIELD_NOTES = path.join(ROOT, "content", "field-notes");
const SOURCES = path.join(ROOT, "content", "sources");

const DEMO_TERMS = /\bseed\b|contoh \(seed\)|\bdummy\b|\bplaceholder\b|bersifat ilustratif|\bilustratif\b/i;
const FIRST_PERSON_FIELD =
  /observasi kami|kami melihat|kami menemukan|kami mengamati|kami amati di lapangan|dari lokasi sebenarnya|kami kunjungi langsung|kami memotret di lapangan|kami mengukur di lapangan/i;
const AI_VISUAL_TERMS =
  /AI-generated|AI generated|generated by AI|Midjourney|DALL-E|Stable Diffusion|gambar AI|citra AI|dibuat AI/i;
const UNCLEAR_DISPLAY_LICENSE =
  /unknown|unclear|unverified|belum jelas|tidak jelas|tidak diketahui|all rights reserved|hak cipta tidak jelas/i;
const DIRECT_MEDIA_URL = /\.(avif|gif|jpe?g|png|svg|webp|mp4|mov|webm)(\?.*)?$/i;
const ISO_DATE = /^\d{4}-\d{2}-\d{2}$/;
const EM_DASH = "\u2014";
const BANNED_TEMPLATE_PHRASES = [
  /Tulisan ini mengambil jalan yang lebih pelan/i,
  /Yang dicari bukan sensasi/i,
  /Sumber yang dipakai pada bagian ini sengaja tidak disamakan bobotnya/i,
  /bukan dari pengalaman lapangan pribadi NaLI/i,
  /batas bukti/i,
];
const METHOD_META_PROSE =
  /NaLI|Basis tulisan|Claim Ledger|sourceIds|arsip sumber|bukti visual eksternal|lisensi|validasi|metodologi|metode NaLI|firstPartyFieldwork|sumber yang dipakai|status bukti/i;

const errors = [];
const warnings = [];

const sourceFiles = new Set(
  fs.existsSync(SOURCES)
    ? fs
        .readdirSync(SOURCES)
        .filter((f) => /\.mdx?$/.test(f))
        .map((f) => f.replace(/\.mdx?$/, ""))
    : [],
);
const articleFiles = new Set(
  fs.existsSync(ARTICLES)
    ? fs
        .readdirSync(ARTICLES)
        .filter((f) => /\.mdx?$/.test(f))
        .map((f) => f.replace(/\.mdx?$/, ""))
    : [],
);

// Jurnal publication records loaded from the TypeScript batch files.
const publications = await loadPublications();
const jurnalSlugs = new Set(publications.map((p) => p.slug));

function read(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir)
    .filter((f) => /\.mdx?$/.test(f))
    .map((f) => {
      const fullPath = path.join(dir, f);
      const { data, content } = matter(fs.readFileSync(fullPath, "utf8"));
      return { file: path.relative(ROOT, fullPath), data, content };
    });
}

function looksLikeDirectMediaUrl(value) {
  if (!value) return false;
  try {
    const url = new URL(String(value));
    if (url.hostname === "commons.wikimedia.org" && url.pathname.startsWith("/wiki/File:")) {
      return false;
    }
    return DIRECT_MEDIA_URL.test(url.pathname);
  } catch {
    return DIRECT_MEDIA_URL.test(String(value));
  }
}

function slugFromFile(file) {
  return file.replace(/^content\/articles\//, "").replace(/^content\/sources\//, "").replace(/\.mdx?$/, "");
}

function wordCount(content) {
  const text = String(content)
    .replace(/```[\s\S]*?```/g, " ")
    .replace(/!\[[^\]]*]\([^)]*\)/g, " ")
    .replace(/\[[^\]]*]\([^)]*\)/g, " ")
    .replace(/[>#*_`|]/g, " ")
    .trim();
  if (!text) return 0;
  return text.split(/\s+/).filter(Boolean).length;
}

function bodyParagraphs(content) {
  return String(content)
    .split(/\n{2,}/)
    .map((p) =>
      p
        .replace(/```[\s\S]*?```/g, " ")
        .replace(/!\[[^\]]*]\([^)]*\)/g, " ")
        .replace(/\[[^\]]*]\([^)]*\)/g, " ")
        .replace(/^#+\s*/gm, "")
        .replace(/[>#*_`|]/g, " ")
        .replace(/\s+/g, " ")
        .trim(),
    )
    .filter((p) => p.length > 40);
}

function scanDuplicateParagraphs(tag, content) {
  const seen = new Map();
  for (const paragraph of bodyParagraphs(content)) {
    const key = paragraph.toLowerCase();
    seen.set(key, (seen.get(key) ?? 0) + 1);
  }
  for (const [paragraph, count] of seen.entries()) {
    if (count > 1) {
      warnings.push(`${tag}: paragraph appears ${count} times: "${paragraph.slice(0, 90)}..."`);
    }
  }
}

function scanMethodProseRatio(tag, content) {
  const paragraphs = bodyParagraphs(content);
  if (!paragraphs.length) return;
  const methodParagraphs = paragraphs.filter((p) => METHOD_META_PROSE.test(p));
  const ratio = methodParagraphs.length / paragraphs.length;
  if (ratio > 0.25) {
    warnings.push(`${tag}: method/meta prose is ${Math.round(ratio * 100)} percent of body paragraphs.`);
  }
}

function scanEmDashes() {
  const skipDirs = new Set([
    ".git",
    ".next",
    ".vercel",
    ".agents",
    ".claude",
    ".codex",
    "node_modules",
    "public",
    ".playwright-mcp",
    "raw",
    "cache",
    "_social",
  ]);
  const textExts = new Set([
    ".css",
    ".example",
    ".html",
    ".js",
    ".json",
    ".jsx",
    ".md",
    ".mdx",
    ".mjs",
    ".sql",
    ".ts",
    ".tsx",
    ".txt",
    ".yaml",
    ".yml",
  ]);
  const textFiles = new Set([
    ".env.example",
    "BACKLOG.md",
    "CLAUDE.md",
    "NALI_MASTER_DOCUMENT_v1_2.md",
    "README.md",
    "package.json",
    "tailwind.config.ts",
  ]);

  function walk(dir) {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      if (skipDirs.has(entry.name)) continue;
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        walk(fullPath);
        continue;
      }
      const rel = path.relative(ROOT, fullPath);
      const ext = path.extname(entry.name);
      if (!textExts.has(ext) && !textFiles.has(entry.name) && !textFiles.has(rel)) continue;
      const raw = fs.readFileSync(fullPath, "utf8");
      if (raw.includes(EM_DASH)) {
        const lines = raw.split(/\r?\n/);
        lines.forEach((line, index) => {
          if (line.includes(EM_DASH)) errors.push(`[em-dash] ${rel}:${index + 1}: remove em dash character.`);
        });
      }
    }
  }

  walk(ROOT);
}

function validateRelatedArticleIds(tag, item, fieldName, articleSlug) {
  if (!Array.isArray(item?.relatedArticleIds) || item.relatedArticleIds.length === 0) {
    errors.push(`${tag}: ${fieldName} missing relatedArticleIds.`);
    return;
  }
  if (!item.relatedArticleIds.includes(articleSlug)) {
    errors.push(`${tag}: ${fieldName} relatedArticleIds must include ${articleSlug}.`);
  }
}

function validateRenderableSrc(tag, item, fieldName) {
  if (!item?.src) {
    errors.push(`${tag}: ${fieldName} missing src for a visible rendered image.`);
    return;
  }
  const src = String(item.src);
  if (src.startsWith("/")) {
    const localPath = path.join(ROOT, "public", src.replace(/^\/+/, ""));
    if (!fs.existsSync(localPath)) {
      errors.push(`${tag}: ${fieldName} src does not exist in public/: ${src}.`);
    }
  }
}

scanEmDashes();

/* ---- Articles ---- */
for (const { file, data, content } of read(ARTICLES)) {
  const published = (data.status ?? "draft") === "published";
  const tag = `[article] ${file}`;
  const articleSlug = String(data.slug ?? slugFromFile(file));

  if (published && DEMO_TERMS.test(content)) {
    errors.push(`${tag}: contains demo/placeholder language.`);
  }
  if (published && DEMO_TERMS.test(JSON.stringify(data))) {
    errors.push(`${tag}: frontmatter contains demo/placeholder language.`);
  }

  for (const pattern of BANNED_TEMPLATE_PHRASES) {
    if (published && (pattern.test(content) || pattern.test(JSON.stringify(data)))) {
      errors.push(`${tag}: contains banned template phrase: ${pattern.source}`);
    }
  }

  if (data.firstPartyFieldwork !== true && FIRST_PERSON_FIELD.test(content)) {
    errors.push(`${tag}: first-person field claim but firstPartyFieldwork is not true.`);
  }

  if (!published) continue;

  const wc = wordCount(content);
  if (wc < 900) warnings.push(`${tag}: body has ${wc} words, below the 900-word quality floor.`);
  scanDuplicateParagraphs(tag, content);
  scanMethodProseRatio(tag, content);

  if (!Array.isArray(data.sources) || data.sources.length === 0)
    errors.push(`${tag}: missing sources.`);
  if (!Array.isArray(data.sourceIds) || data.sourceIds.length === 0) {
    errors.push(`${tag}: missing sourceIds.`);
  } else {
    data.sourceIds.forEach((id, i) => {
      if (!sourceFiles.has(String(id))) errors.push(`${tag}: sourceIds[${i}] does not match a source file: ${id}.`);
    });
  }
  if (!Array.isArray(data.claimLedger) || data.claimLedger.length === 0)
    errors.push(`${tag}: missing claimLedger.`);
  if (!Array.isArray(data.limitations) || data.limitations.length === 0)
    errors.push(`${tag}: missing limitations.`);
  if (!data.confidence) errors.push(`${tag}: missing confidence.`);
  if (!data.evidenceBasis) errors.push(`${tag}: missing evidenceBasis.`);
  if (data.firstPartyFieldwork === undefined)
    errors.push(`${tag}: missing firstPartyFieldwork (must be explicit, usually false).`);
  if (data.firstPartyFieldwork !== false)
    errors.push(`${tag}: firstPartyFieldwork must be false unless separately documented and reviewed.`);

  const hasDisplayedImages = Array.isArray(data.images) && data.images.some((img) => Boolean(img?.src));
  const hasInternalDiagrams = Array.isArray(data.diagrams) && data.diagrams.some((diagram) => Boolean(diagram?.src));
  if (!hasDisplayedImages && !hasInternalDiagrams) {
    errors.push(`${tag}: published article needs a visible displayed image or a rendered internal explanatory diagram/map/timeline image.`);
  }

  if (Array.isArray(data.images)) {
    data.images.forEach((img, i) => {
      for (const k of ["src", "sourceUrl", "license", "attribution", "alt", "caption", "checkedAt"]) {
        if (!img?.[k]) errors.push(`${tag}: image[${i}] missing ${k}.`);
      }
      if (!img?.creator && !img?.institution) {
        errors.push(`${tag}: image[${i}] missing creator or institution.`);
      }
      validateRenderableSrc(tag, img, `image[${i}]`);
      validateRelatedArticleIds(tag, img, `image[${i}]`, articleSlug);
      if (img?.checkedAt && !ISO_DATE.test(String(img.checkedAt))) {
        errors.push(`${tag}: image[${i}] checkedAt must be YYYY-MM-DD.`);
      }
      if (UNCLEAR_DISPLAY_LICENSE.test(String(img?.license ?? ""))) {
        errors.push(`${tag}: image[${i}] has unclear license; move it to externalVisuals and do not display it.`);
      }
      if (looksLikeDirectMediaUrl(img?.sourceUrl)) {
        errors.push(`${tag}: image[${i}] sourceUrl should be a source/license page, not a direct media file.`);
      }
      if (AI_VISUAL_TERMS.test(JSON.stringify(img ?? {}))) {
        errors.push(`${tag}: image[${i}] appears to reference AI-generated imagery, which cannot be evidence.`);
      }
    });
  }

  if (Array.isArray(data.diagrams)) {
    data.diagrams.forEach((diagram, i) => {
      for (const k of ["src", "title", "sourceUrl", "license", "attribution", "alt", "caption", "checkedAt"]) {
        if (!diagram?.[k]) errors.push(`${tag}: diagrams[${i}] missing ${k}.`);
      }
      if (!diagram?.creator && !diagram?.institution) {
        errors.push(`${tag}: diagrams[${i}] missing creator or institution.`);
      }
      validateRenderableSrc(tag, diagram, `diagrams[${i}]`);
      if (!Array.isArray(diagram?.items) || diagram.items.length < 2) {
        errors.push(`${tag}: diagrams[${i}] must list at least two explanatory items.`);
      }
      validateRelatedArticleIds(tag, diagram, `diagrams[${i}]`, articleSlug);
      if (diagram?.checkedAt && !ISO_DATE.test(String(diagram.checkedAt))) {
        errors.push(`${tag}: diagrams[${i}] checkedAt must be YYYY-MM-DD.`);
      }
      if (UNCLEAR_DISPLAY_LICENSE.test(String(diagram?.license ?? ""))) {
        errors.push(`${tag}: diagrams[${i}] has unclear license.`);
      }
      if (looksLikeDirectMediaUrl(diagram?.sourceUrl)) {
        errors.push(`${tag}: diagrams[${i}] sourceUrl should be a cited source page, not a direct media file.`);
      }
      if (!/diagram|peta|linimasa|penjelas/i.test(String(diagram?.caption ?? ""))) {
        warnings.push(`${tag}: diagrams[${i}] caption should clearly label it as an explanatory diagram/map/timeline.`);
      }
      if (AI_VISUAL_TERMS.test(JSON.stringify(diagram ?? {}))) {
        errors.push(`${tag}: diagrams[${i}] appears to reference AI-generated imagery, which cannot be evidence.`);
      }
    });
  }

  if (Array.isArray(data.externalVisuals)) {
    data.externalVisuals.forEach((ev, i) => {
      for (const k of ["title", "sourceUrl", "shows", "limitation", "checkedAt"]) {
        if (!ev?.[k]) errors.push(`${tag}: externalVisuals[${i}] missing ${k}.`);
      }
      if (ev?.checkedAt && !ISO_DATE.test(String(ev.checkedAt))) {
        errors.push(`${tag}: externalVisuals[${i}] checkedAt must be YYYY-MM-DD.`);
      }
      if (looksLikeDirectMediaUrl(ev?.sourceUrl)) {
        errors.push(`${tag}: externalVisuals[${i}] sourceUrl must be the original source page, not a direct media file.`);
      }
      if (!/lisensi|license|tidak menampilkan|not redisplay|not display/i.test(String(ev?.limitation ?? ""))) {
        errors.push(`${tag}: externalVisuals[${i}] limitation must state the license/display constraint.`);
      }
      if (AI_VISUAL_TERMS.test(JSON.stringify(ev ?? {}))) {
        errors.push(`${tag}: externalVisuals[${i}] appears to reference AI-generated imagery, which cannot be evidence.`);
      }
    });
  }

  if (Array.isArray(data.claimLedger)) {
    data.claimLedger.forEach((c, i) => {
      if (!c?.claim) errors.push(`${tag}: claimLedger[${i}] missing claim.`);
      if (!c?.status) errors.push(`${tag}: claimLedger[${i}] missing status.`);
    });
  }
}

/* ---- Field notes ---- */
for (const { file, data, content } of read(FIELD_NOTES)) {
  const published = (data.status ?? "draft") === "published";
  if (!published) continue;
  const tag = `[field-note] ${file}`;
  if (DEMO_TERMS.test(content) || DEMO_TERMS.test(JSON.stringify(data)))
    errors.push(`${tag}: contains demo/placeholder language.`);
  for (const pattern of BANNED_TEMPLATE_PHRASES) {
    if (pattern.test(content) || pattern.test(JSON.stringify(data))) {
      errors.push(`${tag}: contains banned template phrase: ${pattern.source}`);
    }
  }
  if (data.firstPartyFieldwork !== true && FIRST_PERSON_FIELD.test(content))
    errors.push(`${tag}: first-person field claim. Field notes must be framed as third-party/open-source unless separately documented.`);
}

/* ---- Sources ---- */
let sourceCount = 0;
for (const { file, data, content } of read(SOURCES)) {
  sourceCount++;
  const tag = `[source] ${file}`;
  if (!data.title) errors.push(`${tag}: missing title.`);
  const sourceId = data.id ?? file.replace(/^content\/sources\//, "").replace(/\.mdx?$/, "");
  if (!data.id) errors.push(`${tag}: missing id.`);
  if (String(sourceId) !== file.replace(/^content\/sources\//, "").replace(/\.mdx?$/, ""))
    errors.push(`${tag}: id should match the source filename for stable linking.`);
  if (!data.type && !data.sourceType) errors.push(`${tag}: missing sourceType (type).`);
  if (!data.author && !data.institution) errors.push(`${tag}: missing author or institution.`);
  if (
    !data.year &&
    !data.publishedAt &&
    !JSON.stringify(data.limitations ?? "").match(/tanggal publikasi|publication date|tahun terbit/i)
  ) {
    errors.push(`${tag}: missing year/publication date or a limitation explaining why it is unavailable.`);
  }
  if (data.publishedAt && !ISO_DATE.test(String(data.publishedAt)))
    errors.push(`${tag}: publishedAt must be YYYY-MM-DD when present.`);
  if (!content.trim()) errors.push(`${tag}: missing summary body.`);

  const hasReliabilityNote = Boolean(data.reliability || data.reliabilityNote);
  if (!hasReliabilityNote) errors.push(`${tag}: missing reliability note.`);
  if (!data.reliabilityLevel) warnings.push(`${tag}: no structured reliabilityLevel.`);
  if (!data.checkedAt) errors.push(`${tag}: missing checkedAt.`);
  if (data.checkedAt && !ISO_DATE.test(String(data.checkedAt))) {
    errors.push(`${tag}: checkedAt must be YYYY-MM-DD.`);
  }
  if (!data.language) errors.push(`${tag}: missing language.`);
  if (!Array.isArray(data.geography) || data.geography.length === 0)
    errors.push(`${tag}: missing geography.`);
  if (!Array.isArray(data.topics) || data.topics.length === 0)
    errors.push(`${tag}: missing topics.`);
  const claims = data.keyClaimsSupported ?? data.keyClaims;
  if (!Array.isArray(claims) || claims.length === 0)
    errors.push(`${tag}: missing keyClaimsSupported.`);
  const used = data.usedInArticleIds ?? data.usedInArticles;
  const usedJurnal = data.usedInJurnalIds;
  const hasArticleUse = Array.isArray(used) && used.length > 0;
  const hasJurnalUse = Array.isArray(usedJurnal) && usedJurnal.length > 0;
  if (!hasArticleUse && !hasJurnalUse) {
    errors.push(`${tag}: missing usedInArticleIds or usedInJurnalIds.`);
  }
  if (hasArticleUse) {
    used.forEach((id, i) => {
      if (!articleFiles.has(String(id))) errors.push(`${tag}: usedInArticleIds[${i}] does not match an article file: ${id}.`);
    });
  }
  if (hasJurnalUse) {
    usedJurnal.forEach((id, i) => {
      if (!jurnalSlugs.has(String(id))) errors.push(`${tag}: usedInJurnalIds[${i}] does not match a jurnal entry: ${id}.`);
    });
  }

  const hasLink = Boolean(data.url || data.doi || data.archiveUrl);
  const hasLimitation = Array.isArray(data.limitations) && data.limitations.length > 0;
  if (!hasLink) errors.push(`${tag}: missing traceable url, doi, or archiveUrl.`);
  if (!hasLimitation) errors.push(`${tag}: missing limitations.`);
}

/* ---- Jurnal publication catalog ---- */
// Titles that look like the old NaLI-authored knowledge notes (must NOT appear here).
const NOTE_STYLE_TITLE = [
  /perawat hutan/i,
  /sebaran pulaunya/i,
  /tidak mengerami telurnya/i,
  /, perawat /i,
  /yang kehilangan ruang/i,
];
const PUBLICATION_TYPES = new Set([
  "journal_article",
  "report",
  "book",
  "monograph",
  "proceeding",
  "dataset",
  "archive_record",
  "museum_record",
  "institutional_document",
]);
const ACCESS_TYPES = new Set(["open_access", "free_to_read", "official_pdf_available", "metadata_only"]);
// generic journal names that must NOT be a record title on their own
const GENERIC_JOURNAL_TITLE =
  /^(zookeys|plos one|biological conservation|oryx|conservation biology|coral reefs|biodiversity and conservation|nature conservation|cambridge core|springer|elsevier|sciencedirect|treubia|reinwardtia|berita biologi|biodiversitas|marine pollution bulletin|nhess)\b/i;
const PDF_LIKE = /\.pdf($|\?)|\/pdf\b|\/download\b|\/articles\/.+\.pdf|content\/pdf|pdfdirect|article\/file|\/latest\.pdf/i;
let pubWithPdfDownload = 0;
let pubGenericTitles = 0;
const seenPubSlugs = new Map();
const seenPubSynopses = new Map();
const pubTypeCounts = new Map();
const pubPublishers = new Set();
let pubRealCovers = 0;
let pubFallbackCovers = 0;
let pubWithDoi = 0;
let pubWithPdf = 0;
let pubWithDownload = 0;
let pubInvalidNotes = 0;

for (const pub of publications) {
  const tag = `[jurnal] ${pub.__file ?? pub.slug}#${pub.slug ?? "?"}`;
  if (!pub.slug) errors.push(`${tag}: missing slug.`);
  if (!pub.title) errors.push(`${tag}: missing title.`);
  if (!pub.checkedAt) errors.push(`${tag}: missing checkedAt.`);
  if (pub.checkedAt && !ISO_DATE.test(String(pub.checkedAt))) errors.push(`${tag}: checkedAt must be YYYY-MM-DD.`);

  // must be a real external publication record
  if (!pub.sourceUrl) errors.push(`${tag}: missing sourceUrl (must be an external publication).`);
  else if (!/^https?:\/\/.+\..+/.test(String(pub.sourceUrl))) errors.push(`${tag}: sourceUrl is not a traceable URL.`);
  if (!pub.publisherOrInstitution) errors.push(`${tag}: missing publisherOrInstitution.`);
  else pubPublishers.add(pub.publisherOrInstitution);
  if (!pub.publicationType) errors.push(`${tag}: missing publicationType.`);
  else if (!PUBLICATION_TYPES.has(pub.publicationType)) errors.push(`${tag}: unknown publicationType ${pub.publicationType}.`);
  if (!pub.accessType) errors.push(`${tag}: missing accessType.`);
  else if (!ACCESS_TYPES.has(pub.accessType)) errors.push(`${tag}: unknown accessType ${pub.accessType}.`);

  // title must not be a NaLI-authored knowledge note
  if (NOTE_STYLE_TITLE.some((re) => re.test(String(pub.title ?? "")))) {
    errors.push(`${tag}: title looks like a NaLI-authored note, not an external publication: "${pub.title}".`);
    pubInvalidNotes++;
  }
  // title must be a SPECIFIC item, not just a generic journal/collection name
  const titleWords = String(pub.title ?? "").trim().split(/\s+/).length;
  if (
    GENERIC_JOURNAL_TITLE.test(String(pub.title ?? "")) &&
    titleWords <= 5
  ) {
    errors.push(`${tag}: title is a generic journal name, not a specific publication: "${pub.title}".`);
    pubGenericTitles++;
  }
  if (pub.journalOrCollection && String(pub.title ?? "").trim().toLowerCase() === String(pub.journalOrCollection).trim().toLowerCase()) {
    errors.push(`${tag}: title equals the journal/collection name; record must be a specific item.`);
    pubGenericTitles++;
  }

  // synopsis (NaLI's Indonesian summary of the external work)
  const synopsis = String(pub.synopsis ?? "");
  if (!synopsis.trim()) {
    errors.push(`${tag}: missing synopsis.`);
  } else {
    const sw = wordCount(synopsis);
    if (sw < 20) warnings.push(`${tag}: synopsis has ${sw} words, quite short for a publication summary.`);
    if (synopsis.includes(EM_DASH)) errors.push(`${tag}: synopsis contains an em dash.`);
    const synKey = synopsis.replace(/\s+/g, " ").trim().toLowerCase();
    if (seenPubSynopses.has(synKey)) errors.push(`${tag}: duplicate synopsis shared with ${seenPubSynopses.get(synKey)}.`);
    else seenPubSynopses.set(synKey, pub.slug);
  }
  if (!pub.whyItMatters || !String(pub.whyItMatters).trim()) errors.push(`${tag}: missing whyItMatters.`);
  if (!Array.isArray(pub.limitations) || pub.limitations.length === 0) errors.push(`${tag}: missing limitations.`);
  if (!Array.isArray(pub.topics) || pub.topics.length === 0) errors.push(`${tag}: missing topics.`);
  if (!Array.isArray(pub.geography) || pub.geography.length === 0) errors.push(`${tag}: missing geography.`);

  // cover metadata (real image, or documented source-card)
  const cover = pub.cover;
  if (!cover || typeof cover !== "object") {
    errors.push(`${tag}: missing cover metadata.`);
  } else {
    for (const k of ["title", "sourceUrl", "publisherOrInstitution", "license", "displayBasis", "attribution", "alt", "caption"]) {
      if (!cover[k]) errors.push(`${tag}: cover missing ${k}.`);
    }
    const image = cover.localPath ?? cover.imageUrl;
    if (cover.isRealSourceCover) {
      pubRealCovers++;
      if (!image) errors.push(`${tag}: real cover has no localPath/imageUrl.`);
      else if (String(image).startsWith("/")) {
        const p = path.join(ROOT, "public", String(image).replace(/^\/+/, ""));
        if (!fs.existsSync(p)) errors.push(`${tag}: cover image does not exist in public/: ${image}.`);
      }
    } else {
      pubFallbackCovers++;
      if (!cover.fallbackReason) errors.push(`${tag}: source-card fallback cover requires a fallbackReason.`);
    }
    if (AI_VISUAL_TERMS.test(JSON.stringify(cover))) errors.push(`${tag}: cover references AI-generated imagery, not allowed.`);
  }

  // download: PRIMARY must be the real PDF/document/dataset/source, NEVER metadata TXT.
  const dl = pub.download;
  const PRIMARY_KINDS = new Set(["official_pdf", "official_document", "official_dataset", "external_source_only"]);
  if (!dl || !dl.primaryKind || !dl.primaryUrl || !dl.label) {
    errors.push(`${tag}: missing primary download (primaryKind/primaryUrl/label).`);
  } else {
    if (!PRIMARY_KINDS.has(dl.primaryKind)) errors.push(`${tag}: unknown download.primaryKind ${dl.primaryKind}.`);
    if (/\/download\.txt$/.test(String(dl.primaryUrl)) || /metadata/i.test(String(dl.label))) {
      errors.push(`${tag}: primary download must not be the NaLI metadata TXT.`);
    }
    if (dl.primaryKind === "official_pdf") {
      pubWithPdfDownload++;
      if (dl.primaryUrl !== pub.pdfUrl) errors.push(`${tag}: official_pdf primaryUrl must equal pdfUrl.`);
      if (!PDF_LIKE.test(String(dl.primaryUrl))) {
        errors.push(`${tag}: download.primaryKind is official_pdf but URL is not a PDF/document URL: ${dl.primaryUrl}.`);
      }
    } else if (dl.primaryKind === "official_document" || dl.primaryKind === "official_dataset") {
      pubWithPdfDownload++;
      if (!pub.pdfUrl) errors.push(`${tag}: ${dl.primaryKind} requires a pdfUrl/document URL.`);
    }
    // secondary metadata route must still exist
    if (!dl.metadataUrl || dl.metadataUrl !== `/jurnal/${pub.slug}/download.txt`) {
      warnings.push(`${tag}: metadata route is not the expected /jurnal/${pub.slug}/download.txt.`);
    } else {
      pubWithDownload++;
    }
  }

  // related source ids must resolve when present
  for (const id of pub.relatedSourceIds ?? []) {
    if (!sourceFiles.has(String(id))) errors.push(`${tag}: relatedSourceIds "${id}" does not resolve to a source.`);
  }
  for (const id of pub.relatedArticleIds ?? []) {
    if (!articleFiles.has(String(id))) errors.push(`${tag}: relatedArticleIds "${id}" does not resolve to an article.`);
  }

  if (pub.doi) pubWithDoi++;
  if (pub.pdfUrl) pubWithPdf++;
  if (pub.publicationType) pubTypeCounts.set(pub.publicationType, (pubTypeCounts.get(pub.publicationType) ?? 0) + 1);
  if (pub.slug) seenPubSlugs.set(pub.slug, (seenPubSlugs.get(pub.slug) ?? 0) + 1);
}
for (const [slug, count] of seenPubSlugs.entries()) {
  if (count > 1) errors.push(`[jurnal] duplicate publication slug "${slug}" appears ${count} times.`);
}
if (publications.length > 0) {
  const fbRatio = pubFallbackCovers / publications.length;
  if (fbRatio > 0.2) {
    warnings.push(
      `[jurnal] ${pubFallbackCovers}/${publications.length} (${Math.round(fbRatio * 100)}%) covers are source-card fallbacks (target under 20%). Many are commercial-publisher covers with unclear license; add more open-access / institutional publications with displayable covers.`,
    );
  }
}

/* ---- Coverage stats ---- */
let publishedArticleCount = 0;
let articlesWithVisual = 0;
for (const { data } of read(ARTICLES)) {
  if ((data.status ?? "draft") !== "published") continue;
  publishedArticleCount++;
  const hasImg = Array.isArray(data.images) && data.images.some((i) => Boolean(i?.src));
  const hasDiag = Array.isArray(data.diagrams) && data.diagrams.some((d) => Boolean(d?.src));
  if (hasImg || hasDiag) articlesWithVisual++;
}

/* ---- Report ---- */
console.log(`\nEditorial validation summary:`);
console.log(`  sources checked:        ${sourceCount}`);
console.log(`  published articles:     ${publishedArticleCount}`);
console.log(`  article image coverage: ${articlesWithVisual}/${publishedArticleCount}`);
console.log(`  jurnal publications:    ${publications.length}`);
console.log(`  by type:                ${[...pubTypeCounts.entries()].map(([t, n]) => `${t}:${n}`).join(", ") || "none"}`);
console.log(`  distinct publishers:    ${pubPublishers.size}`);
console.log(`  real covers:            ${pubRealCovers}/${publications.length}`);
console.log(`  source-card fallbacks:  ${pubFallbackCovers}/${publications.length}`);
console.log(`  with DOI:               ${pubWithDoi}`);
console.log(`  with PDF/document link: ${pubWithPdf}`);
console.log(`  primary download = PDF/doc/dataset: ${pubWithPdfDownload}/${publications.length}`);
console.log(`  NaLI metadata TXT (secondary): ${pubWithDownload}/${publications.length}`);
console.log(`  generic journal-name titles: ${pubGenericTitles}`);
console.log(`  invalid self-authored notes: ${pubInvalidNotes}`);
console.log(`  em dash status:         ${errors.some((e) => e.startsWith("[em-dash]")) ? "FAIL" : "clean"}`);
if (warnings.length) {
  console.log(`\n${warnings.length} warning(s):`);
  for (const w of warnings) console.log(`  WARN ${w}`);
}
if (errors.length) {
  console.log(`\n${errors.length} error(s):`);
  for (const e of errors) console.log(`  FAIL ${e}`);
  console.log("");
  process.exit(1);
}
console.log("All editorial-content checks passed.\n");
